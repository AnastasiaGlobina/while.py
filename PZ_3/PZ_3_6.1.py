# Вводятся 2 числа, программа показывает четные они или нет
number = int(input ("введите первое число: "))
numbers = int(input ("введите второе число: "))
if  number % 2 == 0:
    print ("Вывод: число 1  является четным ")
else:
    print("Вывод: число 1 является  нечетным" )
if numbers % 2 == 0:
    print("Вывод: число 2  является четным")
else:
    print("Вывод: число 2  является  нечетным")
